export function findNode(id: string, node: FaqNode = FAQ_TREE): FaqNode | null {
  if (node.id === id) return node;
  if (!node.children) return null;
  for (const child of node.children) {
    const found = findNode(id, child);
    if (found) return found;
  }
  return null;
}

export function findParent(id: string, node: FaqNode = FAQ_TREE): FaqNode | null {
  if (!node.children) return null;
  for (const child of node.children) {
    if (child.id === id) return node;
    const found = findParent(id, child);
    if (found) return found;
  }
  return null;
}

export function buildFaqKeyboard(node: FaqNode): InlineKeyboard {
  const rows: InlineKeyboard = [];

  if (node.children) {
    for (const child of node.children) {
      rows.push([{ text: child.title, callback_data: `faq:${child.id}` }]);
    }
  }

  if (node.id !== 'menu') {
    const parent = findParent(node.id);
    const parentId = parent?.id ?? 'menu';
    rows.push([
      { text: '⬅️ Назад', callback_data: `faq:${parentId}` },
      { text: '🏠 В меню', callback_data: 'faq:menu' },
    ]);
  }

  rows.push([{ text: '🆘 Связаться со специалистом', callback_data: 'contact' }]);
  return rows;
}

